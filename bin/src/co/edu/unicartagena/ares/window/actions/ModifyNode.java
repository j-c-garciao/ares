    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Node updt = project.searchNode(current_name);
            if(updt.getName().compareTo(jTextField1.getText())==0){
                updt.setNtype(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()));
                if(project.modifyNode(updt,updt.getId(),0)){
                    JOptionPane.showMessageDialog(this, "Node name "+jTextField1.getText()+" modified!");
                }
                this.dispose();                 
            }else{
                if(!project.existNode(jTextField1.getText())){
                updt.setName(jTextField1.getText());
                updt.setNtype(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()));
                if(project.modifyNode(updt,updt.getId(),0)){
                    JOptionPane.showMessageDialog(this, "Node name "+jTextField1.getText()+" modified!");
                }
                this.dispose();
                } else{
                JOptionPane.showMessageDialog(this, "Node name "+jTextField1.getText()+" already exist in model!");
                this.dispose();
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid data!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

