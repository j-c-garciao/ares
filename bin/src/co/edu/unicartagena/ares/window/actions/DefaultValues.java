    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(jTextField1.getText().matches("^[0-9]+$") && jTextField2.getText().matches("\\d*\\.?\\d+") && Double.parseDouble(jTextField2.getText())>=0  /*&& jTextField3.getText().matches("^[0-9]+$")*/){
                //project.setMax_routes(Integer.parseInt(jTextField1.getText()));
                project.setMax_solutions(Integer.parseInt(jTextField1.getText()));
                //project.setMax_plans(Integer.parseInt(jTextField2.getText()));
                project.setLowerbounds(Double.parseDouble(jTextField2.getText()));
                project.setUpper_bound(Integer.parseInt(jTextField3.getText()));
                
                JOptionPane.showMessageDialog(this, "Default values updated!");
                this.dispose();
            }
            else{
                System.out.println(jTextField1.getText()+" "+jTextField2.getText()+" "+jTextField3.getText());
                if(jTextField1.getText().matches("^[0-9]+$"))
                    System.out.println("T1TRUE");
                
                if(jTextField2.getText().matches("\\d*\\.?\\d+") || jTextField2.getText().matches("\"-?\\\\d+(?:\\\\.\\\\d+)?(?:E-?\\\\d+)?\""))
                    System.out.println("T2TRUE");
                
                if(Double.parseDouble(jTextField2.getText())>=0)
                    System.out.println("T3TRUE");
                
                if(jTextField3.getText().matches("^[0-9]+$"))
                    System.out.println("T4TRUE");
                else
                    System.out.println("T41"+jTextField3.getText());
                
                
                JOptionPane.showMessageDialog(this, "Default values could not be updated!");
                this.dispose();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid data!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

